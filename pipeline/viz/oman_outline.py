def outline_bounds(padding_degrees: float = 0.25) -> dict[str, float]:
    """Return padded lon/lat bounds for rendering the outline and stations."""
    longitudes = [lon for ring in OMAN_OUTLINE_RINGS for lon, _ in ring]
    latitudes = [lat for ring in OMAN_OUTLINE_RINGS for _, lat in ring]
    return {
        "latitude_min": min(latitudes) - padding_degrees,
        "latitude_max": max(latitudes) + padding_degrees,
        "longitude_min": min(longitudes) - padding_degrees,
        "longitude_max": max(longitudes) + padding_degrees,
    }
